//The value of 0 is falsy, so the if statement will not execute the console.log() statement.
if (0) console.log("#1 zero is true");

if ("0") console.log("#2 zero is true");
// The value of null is falsy, so the if statement will not execute the console.log() statement.
if (null) console.log("null is true");

if (-1) console.log("negative is true");

if (1) console.log("positive is true");
